public class 과제1 {
    public  static  void main(String[] args) {


        System.out.println("=============================================================================================================================================================================================");
        int i;
        int j;
        System.out.println(" [구구단 출력] ");
        for (i = 1; i < 10; i++) {
            for (j = 1; j < 10; j++) {
                System.out.print(String.format(" %02d x %02d = %02d     ", j, i, i * j));
            }
            System.out.println();
        }
        System.out.println("=============================================================================================================================================================================================");
    }
}

